package entity;

/**
 *
 * @author PhuongPyke
 */
public class Status_Order {

    private int id;
    private String name;

    public Status_Order() {
    }

    public Status_Order(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Status_Order{" + "id=" + id + ", name=" + name + '}';
    }

}
